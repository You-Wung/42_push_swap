#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *tmp;

	tmp = (char*)s;
	while (*tmp != c)
	{
		if (!*tmp)
			return (NULL);
		tmp++;
	}
	return (tmp);
}
